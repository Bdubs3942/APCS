public class ThirdGradeMathExample
{
    public static void main(String[] args) {

        System.out.println(4 + 6);
        System.out.println("4 plus 6 is " + 4 + 6);
        System.out.println(6 - 4);
        //System.out.println("6 minus 4 is " + 6 - 4);
        System.out.println("6 minus 4 is " + (6 - 4));
        System.out.println(24*4);
        System.out.println("24 times 4 is " + 24 * 4);
        System.out.println("24 times 4 is " + (24 * 4));
        System.out.println(24/6);
        System.out.println("24 divided by 6 is " + 24 / 6);
        System.out.println("24 divided by 4 is " + (24 / 6));

    }
}