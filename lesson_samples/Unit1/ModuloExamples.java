public class ModuloExamples
{
    public static void main(String[] args){
        double modOne = 10 % 5;
        double modTwo = 10 % 2;
        double modThree = 100 % 3;
        double modFour = 943872624 % 4321;

        double aNumber = 3;
        double isThisEven = aNumber % 2;

        System.out.println(modOne);
        System.out.println(modTwo);
        System.out.println(modThree);
        System.out.println(modFour);
        System.out.println(isThisEven);
    }
}