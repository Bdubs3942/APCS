public class IncrementDecrementOperator
{
    public static void main(String[] args)
    {
        int numOfBears = 0;
        System.out.println("There are " + numOfBears + " bears.");
        numOfBears++;
        System.out.println("There are " + numOfBears + " bears.");
        numOfBears++;
        System.out.println("There are " + numOfBears + " bears.");
        numOfBears--;
        System.out.println("There are " + numOfBears + " bears.");
        numOfBears--;
        System.out.println("There are " + numOfBears + " bears.");
    }
}