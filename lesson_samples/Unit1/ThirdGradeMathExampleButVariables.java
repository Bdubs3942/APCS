public class ThirdGradeMathExampleButVariables
{
    public static void main(String[] args) {

        int sampleAdding = 4 + 6;
        int sampleSubtraction = 6 - 4;
        int sampleMultiplication = 24*4;
        int sampleDivision = 24/6;

        System.out.println(sampleAdding);
        System.out.println("4 plus 6 is " + sampleAdding + ".");
        System.out.println(sampleSubtraction);
        System.out.println("6 minus 4 is " + sampleSubtraction + ".");
        System.out.println(sampleMultiplication);
        System.out.println("24 times 4 is " + sampleMultiplication + ".");
        System.out.println(sampleDivision);
        System.out.println("24 divided by 6 is " + sampleDivision + ".");
    }
}