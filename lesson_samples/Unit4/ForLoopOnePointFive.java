public class ForLoopOnePointFive
{
    public static void main(String[] args)
    {
        //Beware the exit conditions, my son! The jaws that bite, the claws that catch!
        
        for(int i = 0; i < 5; i++)
        {
            System.out.println(i);
        }

        System.out.println();

        for(int i = 5; i > 0; i--)
        {
            System.out.println(i);
        }
    }
}